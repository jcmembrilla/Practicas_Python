"""
Los operadores de comparación permiten comparar dos valores 
y devuelven siempre un booleano (true o false)
distinto de !=
mayor o igual >=
menor o igual <=
"""
#ejemplo
x = 10
y = 20

print(x>y) #False
print(x<y) #True
print(x==10) #True


